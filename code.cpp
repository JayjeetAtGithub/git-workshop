#include <bits/stdc++.h>
using namespace std;

int main()
{
    int a = 3;
    int b = 5;
    cout << "Addition : " << a << " + " << b << " = " << a + b << endl;
    cout<<"Multiplication:"<<a<<"*"<<b<<" = "<<a*b<<endl;
    cout << "Division : " << b << " / " << a << " = " << (double)b/a << endl;
    cout << "Subtraction : " << a << " - " << b << " = " << a - b << endl;
} 
